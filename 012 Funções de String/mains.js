console.log("Hello World".toUpperCase()); //Transforma em maiúsculo
console.log("Hello World".toLowerCase()); //Transforma em minúsculo
console.log("Hello World".length); //Quantidade de caracteres
console.log("Hello World".charAt(1)); //Retorna um caracter pela posição
console.log("Hello World".includes('Hello')); //Busca uma string dentro da outra
console.log("Hello World".concat("!")); //Concatena String
console.log("Hello World".slice("0, 5")); //Retorna parte de uma string
console.log("Hello World".split("l")); //Quebra a string baseada em um caracter e transforma em array
console.log("Hello World".replace("World", "Guys")); //Substitui uma palavra por outra
console.log("  Hello World   ".trim()); //Remove espaços no começo e no final da string